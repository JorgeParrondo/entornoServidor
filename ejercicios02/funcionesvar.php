<?php

function sumar ($num1, $num2) {
   $resultado =  $num1 + $num2;
   return $resultado;
}
function restar($num1, $num2){
    $resultado = $num1 - $num2;
    return $resultado;
}
function multiplicar($num1, $num2){
    $resultado = $num1 * $num2;
    return $resultado;
}
function dividir($num1, $num2){
    $resultado = $num1 / $num2;
    return $resultado;
}
function modulo($num1, $num2){
    $resultado = $num1 % $num2;
    return $resultado;
}
function potencia($num1, $num2){
    $resultado = $num1 ** $num2;
    return $resultado;
}

?>